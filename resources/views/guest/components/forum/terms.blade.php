<!-- Button to show modal for demo purposes -->


<script>
    document.addEventListener('DOMContentLoaded', function () {
        const modal = document.getElementById('community_modal');
        const checkbox = document.getElementById('agree_community');
        const acceptBtn = document.getElementById('accept_community_btn');

        // Check if guidelines were already accepted this session
        const accepted = sessionStorage.getItem('communityGuidelinesAccepted');
        if (!accepted) {
            // Show modal after a brief delay if not accepted
            setTimeout(() => {
                modal.showModal();
                modal.dispatchEvent(new Event('show'));
            }, 500);
        }

        // Animate cards when modal shows
        modal.addEventListener('show', function () {
            const cards = modal.querySelectorAll('.card');
            cards.forEach((card, index) => {
                card.style.opacity = '0';
                card.style.transform = 'translateY(20px)';
                setTimeout(() => {
                    card.style.transition = 'all 0.3s ease';
                    card.style.opacity = '1';
                    card.style.transform = 'translateY(0)';
                }, index * 100);
            });
        });

        // Toggle accept button
        checkbox.addEventListener('change', () => {
            if (checkbox.checked) {
                acceptBtn.disabled = false;
                acceptBtn.className = 'btn bg-blue-900 hover:bg-blue-800 text-white btn-sm';
                acceptBtn.innerHTML = '<i class="fas fa-check mr-2 text-yellow-400"></i>I Agree & Join Community';
            } else {
                acceptBtn.disabled = true;
                acceptBtn.className = 'btn bg-gray-400 text-gray-200 btn-sm cursor-not-allowed';
                acceptBtn.innerHTML = '<i class="fas fa-check mr-2"></i>I Agree & Join Community';
            }
        });

        // Accept button logic
        window.acceptCommunityGuidelines = function () {
            modal.close();
            sessionStorage.setItem('communityGuidelinesAccepted', 'true');
            console.log('Community guidelines accepted! User can now access the forum.');
            // You can add redirection to forum here:
            // window.location.href = '/forum';
        };

        // Prevent closing modal by clicking outside
        document.addEventListener('click', function (event) {
            const modalBox = modal.querySelector('.modal-box');
            if (modal.open && !modalBox.contains(event.target)) {
                event.stopPropagation();
                modal.showModal();
            }
        });
    });

    // Manually open modal (for testing)
    function showCommunityModal() {
        const modal = document.getElementById('community_modal');
        modal.showModal();
        modal.dispatchEvent(new Event('show'));
    }

    // Toggle accept button function
    function toggleAcceptButton() {
        const checkbox = document.getElementById('agree_community');
        const acceptBtn = document.getElementById('accept_community_btn');

        if (checkbox.checked) {
            acceptBtn.disabled = false;
            acceptBtn.className = 'btn bg-blue-900 hover:bg-blue-800 text-white btn-sm';
        } else {
            acceptBtn.disabled = true;
            acceptBtn.className = 'btn bg-gray-400 text-gray-200 btn-sm cursor-not-allowed';
        }
    }
</script>